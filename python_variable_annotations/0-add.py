#!/usr/bin/env python3
"""
Module qui contient une fonction pour additionner deux nombres flottants.
"""


def add(a: float, b: float) -> float:
    """
    Additionne deux nombres flottants.

    Cette fonction prend deux nombres à virgule flottante en entrée et retourne
    leur somme. Elle est utilisée pour effectuer des calculs simples de somme.

    Parameters:
    a (float): Le premier nombre flottant à additionner.
    b (float): Le deuxième nombre flottant à additionner.

    Returns:
    float: La somme des deux nombres.
    """

    return a + b
