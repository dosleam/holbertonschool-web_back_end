#!/usr/bin/env python3

def add(a: float, b: float) -> float:
    """
    Cette fonction prend deux nombres à virgule flottante en entrée et
    retourne leur somme

    Parameters:
    a (float): Le premier nombre à ajouter
    b (float): Le deuxième nombre à ajouter

    Returns:
    float: La somme des deux nombres
    """
    return a + b
